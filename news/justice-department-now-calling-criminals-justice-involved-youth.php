            <h1>Justice Department Now Calling Criminals "Justice Involved Youth"</h1>
            <h2>Criminals are criminals no matter what you call them</h2>
            <h2>Jim Hoft | <a href="http://www.thegatewaypundit.com/2016/04/justice-department-now-calling-young-criminals-justice-involved-youth"/>Gateway Pundit</a> - April 30, 2016</h2>
            

<p>This is a classic move by the left. They actually believe if they change the words people use to describe things, it also changes the thing itself. It doesn’t. Criminals are criminals no matter what you call them.</p>

<p> <a href="http://www.investors.com/politics/editorials/obamas-justice-dept-now-calls-young-criminals-justice-involved-youth/">Investor’s Business Daily reports:</a></p>

    <h2>Obama’s Justice Dept. Now Calls Young Criminals ‘Justice-Involved Youth’</h2>

    <p>PC: The Justice Department announced a plan to throw $1.75 million at young criminals in hopes that they don’t become adult criminals. But you’d never know that given the linguistic fog the DOJ has conjured up.</p>

    <p>A press release issued earlier this week describes a new $1.75 million grant program as designed “to help young people involved in the justice system find jobs and housing.”</p>

    <p>Wait, you ask, if these young people are “involved” in the justice system, doesn’t that mean they already have jobs? Like, say, as a trainee in the dispatcher’s office, or a desk clerk in the attorney general’s office, or maybe a janitor at the local courthouse?</p>

    <p>Not exactly. In this case, “justice-involved youth” is what we’re now supposed to call juvenile delinquents, also known as young criminals.</p>

    <p>This is not a euphemism run amok as much as it is an exercise in obfuscation. After all, euphemisms at least try to convey the same meaning, just in a less harsh way, as the words they replace. Such as: “substandard housing” for slum, or “differently abled” for crippled. But being “involved” in something can mean anything. An arsonist could be described as a person who is “involved in the home building industry.” A drunk driver will likely end up “involved in traffic enforcement.”</p>

    <p>To be fair, the administration didn’t, as some have claimed, coin the “justice-involved youth” term. It’s been tossed around academic circles for years. But this administration has embraced this sort of verbal legerdemain.</p>




