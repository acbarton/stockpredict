            <h1>Establishment Denounces Trump Foreign Policy Speech</h1>
            <h2>Kurt Nimmo | Infowars.com - April 28, 2016</h2>
            <p>Presidential frontrunner Donald Trump sent shockwaves through the foreign policy establishment on Wednesday when he delivered a major address at the Mayflower Hotel in Washington.</p>
  
  <p>“America is going to be strong again; America is going to be great again; it’s going to be a friend again,” Trump said. “We’re going to finally have a coherent foreign policy, based on American interests and the shared interests of our allies.”</p>  
  <p>“The world must know that we do not go abroad in search of enemies, that we are always happy when old enemies become friends and when old friends become allies. That’s what we want: We want to bring peace to the world.”</p>
  <p>Trump castigated Obama and political rival Clinton for advancing the foreign policy objectives of the establishment. “Our foreign policy is a complete and total disaster. No vision. No purpose. No direction. No strategy,” he said. ‘We’ve made the Middle East more unstable and chaotic than ever before.”</p>
  <p>He took the Obama administration and specifically former Secretary of State Clinton to task for the destruction of Libya and the murder of thousands of the North Africa nation’s citizens. The intervention, he argued, allowed the Islamic State to gain a foothold in the country.</p>
  <p>The Republican frontrunner criticized Europe and Asia for not taking responsibility for their own defense and promised to repair the nation’s relationship with Russia.</p>
  <p>He said “unlike other candidates for the presidency, foreign aggression will not be my first instinct” and described the war against the Islamic State as much a philosophical struggle as a military one. “Our friends and enemies must know that if I draw a line in the sand, I will enforce that line in the sand—believe me,” he promised.</p>
  <p>Reaction from the political class and the corporate media was sharply critical.</p>
  <p>“Ronald Reagan must be rolling over in his grave,” mused South Carolina Senator and leading neocon, Lindsey Graham. “This speech was unnerving. It was pathetic in its content, and it was scary in terms of its construct. If you had any doubt that Donald Trump is not fit to be commander in chief, this speech should’ve removed it,” Graham said. “It took every problem and fear I have with Donald Trump and put in on steroids.”</p>
  <p>Graham said Trump aped Rand Paul. “It was like a guy from New York reading a speech that somebody wrote for him that he edited that makes no sense… It was not a conservative speech. This was a blend of random thoughts built around Rand Paul’s view of the world.”</p> </div>       
            
        
        
        
        
