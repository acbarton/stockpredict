            <h2>Kit Daniels | <a href="http://www.infowars.com/trump-links-cruzs-father-to-jfk-assassination/"/>Infowars</a> - May 3, 2016</h2>
            

<p>Donald Trump pointed out the possible link between Ted Cruz’s father and Lee Harvey Oswald, referencing an investigative report by Infowars’ Wayne Madsen.</p>
<p>“His father was with Lee Harvey Oswald prior to Oswald's being — you know, shot. I mean, the whole thing is ridiculous,” Trump said Tuesday during a phone interview with Fox News. “What is this, right prior to his being shot, and nobody even brings it up. They don't even talk about that.”</p>
<p>“That was reported, and nobody talks about it.”</p>
<p>Trump was speaking about the exclusive report from Madsen, brought to public attention weeks ago by both Infowars and the National Enquirer.</p>
<p>“A Cuban hired by alleged JFK assassin Lee Harvey Oswald and who bears a striking resemblance to Cruz is seen in an iconic photograph of Oswald and a group of Cubans Oswald hired who were distributing ‘Hands off Cuba!’ pamphlets in New Orleans in the summer of 1963,” Madsen reported. “The photo of Oswald and other Cubans he hired for the Fair Play for Cuba Committee was taken outside the International Trade Mart in New Orleans on August 16, 1963.”</p>
<img src="images/IMG_1546.JPG" />
<p>“Wayne Madsen Reports has been informed by a source that the individual to Oswald’s left is none other than Rafael Cruz; the photograph at the trade mart was favorably compared to a 1954 photograph of Cruz attached to an official Cuban Ministry of Education document.”</p>
<img src="images/IMG_1545.JPG" />
<p>The mainstream media has since attacked the National Enquirer in particular over the story, yet it’s worth pointing out that the tabloid gained notoriety for being the first to release certain details of the Bill Clinton/Monica Lewinsky affair to the public.</p>
<p>And this isn’t just mere speculation over a single photo; previous questions have also been raised about the Cruz family’s connections to CIA operations in Cuba at the time, which likely opened the door for Ted Cruz’s rise within the Bush dynasty.</p>
<img src="images/rbcruz5.jpg" />
<p>For one thing, Rafael’s draft card from July 26, 1967 lists his employer as “Geophysics & Computer Service Inc.,” a French-based firm connected to both Schlumberger and Zapata Offshore Company, the former having a since-declassified relationship with the CIA and the latter once run by George H.W. Bush.</p>
<img src="images/RVCruz4.jpg" />
<p>And the date July 26 holds significance to Cubans; the 26th of July Movement was the revolutionary organization led by Fidel Castro which took over Cuba in 1959.</p>
<p>“RCA, which employed Rafael’s father, was a major player in CIA operations throughout the world, and especially in Latin America, where it helped install CIA clandestine radio stations and operated signals intelligence bases for the U.S. National Security Agency,” Madsen reported. “RCA's chairman and CEO, General David Sarnoff, was involved in a number of anti-Communist fronts linked to the CIA."</p>
<p>“Sarnoff also directed his NBC network to help train CIA agents in developing anti-communist propaganda.”</p>

